%Points: (1,1), (2,3), (3,2), (4,1)

%x intervals
x1 = 1:0.1:2;
x2 = 2:0.1:3;
x3 = 3:0.1:4;

%Cubic spline curves generated from points
y1 = (-12/15)*((x1-1).^3) + (42/15)*(x1-1) + 1;
y2 = ((x2-2).^3) - (12/5)*((x2-2).^2) + (6/15)*(x2-2) + 3;
y3 = (-1/5)*((x3-3).^3) + (3/5)*((x3-3).^2) - (21/15)*(x3-3) + 2;

%Plot
plot(x1, y1, x2, y2, x3, y3);